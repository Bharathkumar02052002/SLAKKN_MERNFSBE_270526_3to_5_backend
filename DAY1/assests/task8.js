// TASK 8 : TYPE CONVERSION CHALLENGE


console.log("10" + 5);


// OUTPUT
// 105

// Reason
// String + Number = String Concatenation

// "10" + "5"
// => "105"





console.log("10" - 5);


// OUTPUT
// 5

// Reason
// - operator converts string into number

// 10 - 5
// => 5





console.log(true + 1);


// OUTPUT
// 2

// Reason
// true = 1

// 1 + 1
// => 2





console.log(false + 10);


// OUTPUT
// 10

// Reason
// false = 0

// 0 + 10
// => 10





console.log(Number("100"));


// OUTPUT
// 100

// Reason
// Number() converts string into number





console.log(String(500));


// OUTPUT
// "500"

// Reason
// String() converts number into string





// IMPORTANT TYPE CONVERSIONS

// true  -> 1
// false -> 0

// Number("10") -> 10
// String(10)  -> "10"




// FINAL OUTPUTS

// 105
// 5
// 2
// 10
// 100
// "500"