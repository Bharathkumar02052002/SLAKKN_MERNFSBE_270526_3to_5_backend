// VAR (can Reassign,can Redeclare)
var city = "Chennai";
console.log(city);

city = "Bangalore";
console.log(city);

var city = "Mumbai";
console.log(city);


// LET (can Reassign,can't Redeclare)
let age = 23;
console.log(age);

age = 24;
console.log(age);

